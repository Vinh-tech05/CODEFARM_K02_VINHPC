/**
 * Array
 * - Hàm
 * + Anonymous function (hàm k tên dên được dùng trong ngữ cảnh cụ thể)
 * + Arrow function(hàm mũi tên)
 */

//Declaration function ( hàm khai báo )
function sum(a, b) {
  return a + b;
}

//Expression function (hàm không có tên được gán cho 1 biến thông qua phép gán )
const sum2 = function () {
  return a + b;
};
